export default function AdminLayout({ children }: { children: React.ReactNode }) {
  const { isLoaded, isSignedIn } = useUser();
  const router = useRouter();
  const pathname = usePathname();
  const [isAdmin, setIsAdmin] = useState<boolean | null>(null);

  useEffect(() => {
    if (isLoaded && !isSignedIn) {
      router.push('/sign-in');
      return;
    }
    if (isLoaded && isSignedIn) {
      // Check admin role via API
      fetch('/api/user/profile')
        .then((r) => (r.ok ? r.json() : null))
        .then((data) => {
          if (data?.role === 'admin') {
            setIsAdmin(true);
          } else {
            setIsAdmin(false);
            router.push('/dashboard');
          }
        })
        .catch(() => {
          setIsAdmin(false);
          router.push('/dashboard');
        });
    }
  }, [isLoaded, isSignedIn, router]);

  if (!isLoaded || isAdmin === null) {
    return (
      <div className="min-h-screen flex items-center justify-center">
        <div className="animate-spin rounded-full h-8 w-8 border-b-2 border-red-500" />
      </div>
    );
  }

  if (!isAdmin) return null;

  return (
    <div className="min-h-screen flex bg-gray-950">
      {/* Sidebar */}
      <aside className="w-64 bg-gray-900 border-r border-gray-800 flex flex-col shrink-0">
        <div className="h-16 flex items-center gap-2 px-5 border-b border-gray-800">
          <span className="text-lg font-bold text-white">ResumeAI</span>
          <span className="text-[10px] font-bold bg-red-500 text-white px-1.5 py-0.5 rounded">
            ADMIN
          </span>
        </div>
        <nav className="flex-1 py-4 space-y-1 px-3">
          {adminNav.map((item) => {
            const isActive = pathname === item.href;
            return (
              <Link
                key={item.href}
                href={item.href}
                className={`flex items-center gap-3 px-3 py-2.5 rounded-lg text-sm font-medium transition ${
                  isActive
                    ? 'bg-red-600 text-white'
                    : 'text-gray-400 hover:bg-gray-800 hover:text-white'
                }`}
              >
                <svg className="w-5 h-5 shrink-0" fill="none" viewBox="0 0 24 24" strokeWidth={1.5} stroke="currentColor">
                  <path strokeLinecap="round" strokeLinejoin="round" d={item.icon} />
                </svg>
                <span>{item.label}</span>
              </Link>
            );
          })}
        </nav>
        <div className="p-4 border-t border-gray-800">
          <Link href="/dashboard" className="text-xs text-gray-500 hover:text-gray-300 transition">
            Back to Dashboard
          </Link>
        </div>
      </aside>

      {/* Main */}
      <main className="flex-1 p-8 overflow-auto text-white">{children}</main>
    </div>
  );
}
